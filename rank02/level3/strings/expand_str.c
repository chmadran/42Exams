#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	if (ac == 2)
	{
		while (av[1][i] == 32 || av[1][i] >= 9 && av[1][i] <= 13)
			i++;
		while (av[1][i])
		{
			if (av[1][i] == 32 || av[1][i] >= 9 && av[1][i] <= 13)
				count = 1;
			if (!(av[1][i] == 32 || av[1][i] >= 9 && av[1][i] <= 13))
			{
				if (count == 1)
					write(1, "   ", 3);
				count = 0;
				write(1, &av[1][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
